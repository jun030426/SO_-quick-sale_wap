function formatKoreanDateTime(dateString) {
  return new Intl.DateTimeFormat("ko-KR", {
    year: "numeric",
    month: "2-digit",
    day: "2-digit",
    hour: "2-digit",
    minute: "2-digit",
    hour12: false,
  }).format(new Date(dateString));
}

function mapDigest(digest) {
  return {
    ...digest,
    generatedLabel: formatKoreanDateTime(digest.generatedAt),
  };
}

export function getAiDigestArchive() {
  return aiDigestArchive.map(mapDigest);
}

